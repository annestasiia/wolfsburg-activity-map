// selectedDay ("Mon"…"Sun") → 'weekday' | 'saturday' | 'sunday'
export function dayToDayType(selectedDay) {
  if (selectedDay === 'Sun') return 'sunday'
  if (selectedDay === 'Sat') return 'saturday'
  return 'weekday'
}

// selectedTime ("HH:MM") → integer hour 0-23
export function timeToHour(selectedTime) {
  return parseInt(selectedTime.split(':')[0], 10)
}

// Main lookup: returns opacity value 0.15–1.0
export function getTrafficOpacity(highwayType, selectedDay, selectedTime) {
  const pattern = TRAFFIC_PATTERNS[highwayType] ?? TRAFFIC_PATTERNS.unclassified
  const dayType = dayToDayType(selectedDay)
  const hour    = timeToHour(selectedTime)
  const level   = pattern[dayType][hour]
  return 0.15 + level * 0.85
}
